-- ============================================================
-- DEPLOYMENTS
-- ============================================================

CREATE TABLE IF NOT EXISTS deployments (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  client_company_id uuid NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  platform_id uuid NOT NULL REFERENCES deployment_platforms(id),
  version text NOT NULL,
  build_number text NOT NULL,
  release_notes text DEFAULT '',
  status text NOT NULL DEFAULT 'draft' CHECK (status IN ('draft', 'submitted', 'in_review', 'approved', 'rejected', 'live', 'rollback')),
  submitted_by uuid REFERENCES profiles(id),
  submitted_at timestamptz,
  approved_by uuid REFERENCES profiles(id),
  approved_at timestamptz,
  live_at timestamptz,
  rollback_at timestamptz,
  rollback_reason text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_deployments_client ON deployments(client_company_id);
CREATE INDEX IF NOT EXISTS idx_deployments_platform ON deployments(platform_id);
CREATE INDEX IF NOT EXISTS idx_deployments_status ON deployments(status);
CREATE INDEX IF NOT EXISTS idx_deployments_created ON deployments(created_at);


/*
  # Agency Structure: Multi-client management for admins

  Enables admins to manage multiple clients, with deployment tracking,
  test platform management, and reporting capabilities.

  New tables:
  - client_companies: Maps admin (company) to their client companies
  - deployment_platforms: Available platforms (iOS, Android, Web, etc)
  - deployments: Deployment records per client
  - test_platforms: Test services (TestFlight, TestFairy, etc)
  - test_builds: Test builds per client
  - reports: Reports generated by admins
  - developer_access_roles: Define which devs have cross-admin access
*/

-- ============================================================
-- CLIENT_COMPANIES (Admin → Client relationship)
-- ============================================================

CREATE TABLE IF NOT EXISTS client_companies (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  admin_company_id uuid NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  client_company_id uuid NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  client_contact_name text NOT NULL DEFAULT '',
  client_contact_email text NOT NULL DEFAULT '',
  client_contact_phone text NOT NULL DEFAULT '',
  notes text DEFAULT '',
  status text NOT NULL DEFAULT 'active' CHECK (status IN ('active', 'inactive', 'suspended')),
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  UNIQUE(admin_company_id, client_company_id)
);

CREATE INDEX IF NOT EXISTS idx_client_companies_admin ON client_companies(admin_company_id);
CREATE INDEX IF NOT EXISTS idx_client_companies_client ON client_companies(client_company_id);

ALTER TABLE client_companies ENABLE ROW LEVEL SECURITY;

CREATE POLICY "client_companies_admin_select"
  ON client_companies FOR SELECT TO authenticated
  USING (
    admin_company_id = get_my_company() AND get_my_role() = 'admin'
    OR client_company_id = get_my_company() AND get_my_role() IN ('admin','agent','developer')
    OR get_my_role() = 'superadmin'
  );

CREATE POLICY "client_companies_admin_insert"
  ON client_companies FOR INSERT TO authenticated
  WITH CHECK (admin_company_id = get_my_company() AND get_my_role() = 'admin');

CREATE POLICY "client_companies_admin_update"
  ON client_companies FOR UPDATE TO authenticated
  USING (admin_company_id = get_my_company() AND get_my_role() = 'admin')
  WITH CHECK (admin_company_id = get_my_company() AND get_my_role() = 'admin');

CREATE POLICY "client_companies_admin_delete"
  ON client_companies FOR DELETE TO authenticated
  USING (admin_company_id = get_my_company() AND get_my_role() = 'admin');


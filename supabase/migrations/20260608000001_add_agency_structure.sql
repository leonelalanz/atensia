-- ============================================================
-- Helper function: Get admin's clients
-- ============================================================

CREATE OR REPLACE FUNCTION get_my_clients()
RETURNS TABLE (client_id uuid, client_name text)
LANGUAGE sql
SECURITY DEFINER
STABLE
AS $$
  SELECT c.id, c.name
  FROM companies c
  INNER JOIN client_companies cc ON c.id = cc.client_company_id
  WHERE cc.admin_company_id = get_my_company()
  AND get_my_role() = 'admin';
$$;

-- ============================================================
-- Helper function: Get developer's accessible companies
-- ============================================================

CREATE OR REPLACE FUNCTION get_dev_accessible_companies()
RETURNS TABLE (company_id uuid)
LANGUAGE sql
SECURITY DEFINER
STABLE
AS $$
  SELECT DISTINCT company_id FROM profiles WHERE id = auth.uid()
  UNION
  SELECT DISTINCT client_company_id FROM client_companies
  WHERE admin_company_id IN (
    SELECT get_my_company() WHERE get_my_role() = 'developer'
  );
$$;
